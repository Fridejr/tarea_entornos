def op_or(n1,n2):
    return f'El resultado es {n1 | n2}'

def op_and(n1,n2):
    return f'El resultado es {n1 & n2}'

def op_exOr(n1,n2):
    return f'El resultado es {n1 ^ n2}'

def op_shiftL(n1,n2):
    return f'El resultado es {n1 << n2}'

def op_shiftR(n1,n2):
    return f'El resultado es {n1 >> n2}'

def op_not(n1):
    return ~ n1